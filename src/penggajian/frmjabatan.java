    private void GetData()  throws SQLException
    {
        Object[] kolom = {"kode", "keterangan", "gapok", "tjabatan", "transport", "tmakan"};
        DefaultTableModel tabmode = new DefaultTableModel(null, kolom);
        ResultSet rs = cl.ViewByAll();
        while(rs.next())
        {
            String data[]={rs.getString("kodejabatan"),rs.getString("keterangan"),rs.getString("gapok"),rs.getString("tjabatan"), rs.getString("transport"), rs.getString("tmakan")};
            tabmode.addRow(data);
        }
        jTable1.setModel(tabmode);
    }

