    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
            cl.setKd(txtkd.getText());
            cl.setKet(txtket.getText());
            cl.setGp(Double.valueOf(txtgp.getText()));
            cl.setJb(Double.valueOf(tjabatan.getText()));
            cl.setTransport(Double.valueOf(txtransport.getText()));
            cl.setMakan(Double.valueOf(tmakan.getText()));
            boolean h = false;
            try
            {
                if(databaru == true)
                {
                    h = cl.Insert();
                }
                else
                {
                    h = cl.Update();
                }
                JOptionPane.showMessageDialog(null,"Penyimpanan berhasil");
                this.GetData();
                this.txtkd.setText("");
                this.txtket.setText("");
                this.txtgp.setText("");
                this.tjabatan.setText("");
                this.txtransport.setText("");
                this.tmakan.setText("");
                this.txtkd.grabFocus();
            } catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Penyimpanan gagal" + ex.getMessage());
            }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        if("".equals(this.txtkd.getText()))
        {
            JOptionPane.showMessageDialog(null, "Silahkan klik pada grid untuk pilih data");
        }
        else
        {
            boolean h = false;
            try
            {
                cl.setKd(this.txtkd.getText());
                h = cl.Delete();
                JOptionPane.showMessageDialog(null, "Penghapusan data berhasil");
                GetData();
                this.txtkd.setText("");
                this.txtket.setText("");
                this.txtgp.setText("");
                this.tjabatan.setText("");
                this.txtransport.setText("");
                this.tmakan.setText("");
                this.txtkd.grabFocus();
               
            } catch(SQLException ex)
            {
                Logger.getLogger(frmjabatan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

