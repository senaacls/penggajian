    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        databaru = false;
        int row = jTable1.getSelectedRow();
        String tabel_klik =(jTable1.getModel().getValueAt(row, 0).toString());
        cl.setKd(tabel_klik);
        try
        {           
                ResultSet rs = cl.ViewById();
                if(rs.next())
                {
                    cl.setKd(rs.getString("kodejabatan"));
                    cl.setKet(rs.getString("keterangan"));
                    cl.setGp(Double.valueOf(rs.getString("gapok")));
                    cl.setJb(Double.valueOf(rs.getString("tjabatan")));
                    cl.setTransport(Double.valueOf(rs.getString("transport")));
                    cl.setMakan(Double.valueOf(rs.getString("tmakan")));
                    
                    this.txtkd.setText(cl.getKd());
                    this.txtket.setText(cl.getKet());
                    this.txtgp.setText(String.format("%.2f",cl.getGp()));
                    this.tjabatan.setText(String.format("%.2f",cl.getJb()));
                    this.txtransport.setText(String.format("%.2f",cl.getTransport()));
                    this.tmakan.setText(String.format("%.2f",cl.getMakan()));
                    this.txtkd.setEditable(false);
                }
        }catch(SQLException ex)
                {
                    Logger.getLogger(frmjabatan.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_jTable1MouseClicked

