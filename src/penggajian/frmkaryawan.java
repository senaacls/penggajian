    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        databaru = false;
        int row = jTable1.getSelectedRow();
        String tabel_klik =(jTable1.getModel().getValueAt(row, 0).toString());
        cl.setNip(tabel_klik);
        try
        {           
                ResultSet rs = cl.ViewByKode();
                if(rs.next())
                {
                    cl.setNip(rs.getString("nip"));
                    cl.setNama(rs.getString("nama"));
                    cl.setSt(Integer.parseInt(rs.getString("jekel")));
                    cl.setTmp(rs.getString("tmplahir"));
                    cl.setTgl(rs.getString("tgl_lahir"));
                    cl.setKd(rs.getString("kodejabatan"));
                    
                    this.txtnip.setText(cl.getNip());
                    this.txtnama.setText(cl.getNama());
                    
                    if(cl.getSt() == 1)
                    {
                        this.rbpria.setSelected(true);
                        this.rbwanita.setSelected(false);
                    }
                    else
                    {
                        this.rbpria.setSelected(false);
                        this.rbwanita.setSelected(true);
                    }
                    
                    this.txtmp.setText(cl.getTmp());
                    this.txtgl.setText(cl.getTgl());
                    this.cmbkd.setSelectedItem(cl.getKd());
                    this.txtnip.setEditable(false);
                }
        }catch(SQLException ex)
                {
                    Logger.getLogger(frmjabatan.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        try
        {
            // TODO add your handling code here:
            HashMap<String, String> map = GetKd();
            String c = (map.get(cmbkd.getSelectedItem().toString()));
            cl.setNip(txtnip.getText());
            cl.setNama(txtnama.getText());
            cl.setTmp(txtmp.getText());
            cl.setTgl(txtgl.getText());
            cl.setKd(c);
            int st = 0;
            if(rbpria.isSelected())
            {
                st = 1;
            }
            else
            {
                st = 0;
            }
            cl.setSt(st);
            boolean h = false;
            try
            {
                if(databaru == true)
                {
                    h = cl.Insert();
                }
                else
                {
                    h = cl.Update();
                }
                JOptionPane.showMessageDialog(null,"Penyimpanan berhasil");
                this.GetData();
                this.txtnip.setText("");
                this.txtnama.setText("");
                this.rbpria.setSelected(false);
                this.rbwanita.setSelected(false);
                this.txtmp.setText("");
                this.txtgl.setText("");
                this.txtnip.grabFocus();
                GetItem();
            } catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Penyimpanan gagal" + ex.getMessage());
            }
        } catch(SQLException ex)
        {
            Logger.getLogger(frmkaryawan.class.getName()).log(Level.SEVERE,null, ex);
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        if("".equals(this.txtnip.getText()))
        {
            JOptionPane.showMessageDialog(null, "Silahkan klik pada grid untuk pilih data");
        }
        else
        {
            boolean h = false;
            try
            {
                cl.setNip(this.txtnip.getText());
                h = cl.Delete();
                JOptionPane.showMessageDialog(null, "Penghapusan data berhasil");
                this.txtnip.setText("");
                this.txtnama.setText("");
                this.rbpria.setSelected(false);
                this.rbwanita.setSelected(false);
                this.txtmp.setText("");
                this.txtgl.setText("");
                this.txtnip.grabFocus();
                GetData();
                GetItem();
            } catch(SQLException ex)
            {
                Logger.getLogger(frmkaryawan.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed

