    private void GetData()  throws SQLException
    {
        Object[] kolom = {"Nip", "Nama", "Jekel", "Tempat Lahir", "Tanggal Lahir", "Kode Jabatan"};
        DefaultTableModel tabmode = new DefaultTableModel(null, kolom);
        ResultSet rs = cl.ViewByAll();
        while(rs.next())
        {
            String data[]={rs.getString("nip"),rs.getString("nama"),rs.getString("jekel"),rs.getString("tmplahir"), rs.getString("tgl_lahir"), rs.getString("kodejabatan")};
            tabmode.addRow(data);
        }
        jTable1.setModel(tabmode);
    }
    
    private HashMap<String, String> GetKd() throws SQLException
    {
        HashMap<String, String> map = new HashMap<String, String>();
        ResultSet rs = cl.ViewKd();
        while(rs.next())
        {
           String kd    = rs.getString("kodejabatan");
           String nama  = rs.getString("keterangan");
           ItemJabatan c = new ItemJabatan(kd,nama);
           map.put(c.getName(), c.getId());
        }

        return map;
    }
    
    public void GetItem() throws SQLException
    {
        cmbkd.removeAllItems();
        HashMap<String, String> map = GetKd();
        for(String s : map.keySet())
        {
           cmbkd.addItem(s);
        }
    }
